/** 해당 날짜의 공휴일 이름 반환. 공휴일이 아니면 null. */
export function getHolidayName(date: string, customHolidays: string[] = []): string | null {
  const mmdd = date.slice(5);
  if (FIXED_ANNUAL[mmdd]) return FIXED_ANNUAL[mmdd];
  if (VARIABLE_HOLIDAYS[date]) return VARIABLE_HOLIDAYS[date];
  if (customHolidays.includes(date)) return '사용자 지정 공휴일';
  return null;
}

export function isPublicHoliday(date: string, customHolidays: string[] = []): boolean {
  return getHolidayName(date, customHolidays) !== null;
}
